    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        if (cbxArea.getSelectedIndex() > 0 && cbxCargo.getSelectedIndex() > 0) {
            String datos[] = getDatos();
            int respuesta = bd_insert.insertTrabajador(datos);
            System.out.println(respuesta);
            if (respuesta == 200) {
                JOptionPane.showMessageDialog(rootPane, "Trabajador registrado correctamente");
                limpiarDatos();
            }
        }
    }//GEN-LAST:event_btn_registrarActionPerformed

    private void cbxAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxAreaActionPerformed
        if (cbxArea.getSelectedIndex() > 0) {
            ArrayList<String> lista = consulta.getPuestos(String.valueOf(cbxArea.getSelectedItem()));
            cbxCargo.removeAllItems();
            cbxCargo.addItem("<--seleccionar-->");
            for (int i = 0; i < lista.size(); i++) {
                cbxCargo.addItem(lista.get(i));
            }
        }
    }//GEN-LAST:event_cbxAreaActionPerformed

