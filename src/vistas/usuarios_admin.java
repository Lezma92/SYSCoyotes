    public void setCbxArea() {
        String datos[] = consulta.getAreas();
        cbxArea.removeAllItems();
        cbxArea.addItem("<--seleccionar-->");
        for (int i = 0; i < datos.length; i++) {
            cbxArea.addItem(datos[i]);
        }
    }
    public void limpiarDatos(){
        txtNombre.setText("");
        txtApp.setText("");
        txtNumero.setText("");
        cbxArea.setSelectedIndex(0);
        cbxCargo.setSelectedIndex(0);
        
    }

    public String[] getDatos() {
        String datos[] = new String[4];
        try {
            datos[0] = cbxCargo.getSelectedItem().toString();
            datos[1] = txtNombre.getText();
            datos[2] = txtApp.getText();
            datos[3] = String.valueOf(Long.parseLong(txtNumero.getText()));

        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
        return datos;
    }

